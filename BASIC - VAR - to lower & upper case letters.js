/*

!!BASIC - VAR - LOWER & UPPER CASE!!

*.toLowerCase - Make a string lowercase
	> string.toLowerCase()
*.toUpperCase - Make a string uppercase
	> string.toUpperCase()

*/

var string = "Hello World!";
var result = string.toLowerCase();
console.log(result);
// hello world!

var string = "Hello World!";
var result = string.toUpperCase();
console.log(result);
// HELLO WORLD!